public class LogicalOp {
    public static void main(String[] args) {
        int a = 10 ,b = 20;
        System.out.println((a<b)&&('c' == 'c'));   //Give true if both statements are true.(&&)
        System.out.println((a>b)||(9<11));     //Give ture if any one statement is true.(||)
        System.out.println(a!=b);


    }
}
