public class RelationOp {
    public static void main(String[] args) {
        int A = 10;
        // A = A + 40;             //This is slow .
        A += 40;                   // This is more faster in java ....Saves Execution Time.
        A -=  4;
        A *= 10;
        A /=  2;
        System.out.println(A);
    }
    
}
